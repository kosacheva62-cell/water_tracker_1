/// Склонение слова "стакан" в ИМЕНИТЕЛЬНОМ падеже (для окна выбора цели и статистики)
/// Возвращает ТОЛЬКО форму слова без числа
/// Примеры: 1 → "стакан", 2 → "стакана", 5 → "стаканов"
String pluralizeGlasses(int n) {
  if (n % 10 == 1 && n % 100 != 11) return 'стакан';
  if (n % 10 >= 2 && n % 10 <= 4 && (n % 100 < 10 || n % 100 >= 20)) return 'стакана';
  return 'стаканов';
}

/// Склонение слова "стакан" в РОДИТЕЛЬНОМ падеже (для прогресс-бара "X/Y стакан(ов/а)")
/// Возвращает ТОЛЬКО форму слова без числа
/// Примеры: 1 → "стакана", 2 → "стаканов", 21 → "стакана", 22 → "стаканов"
String pluralizeGlassesGenitive(int n) {
  // Правило родительного падежа: 
  // - 1, 21, 31... → "стакана" (единственное число)
  // - Все остальные → "стаканов" (множественное число)
  if (n % 10 == 1 && n % 100 != 11) {
    return 'стакана';
  } else {
    return 'стаканов';
  }
}